// Khai báo biến sử dụng var
var conSoBatKi = 3;
console.log(conSoBatKi);
// Khai báo biến sử dụng let
let myName = "Tran Dang Khoi";
console.log(myName);
myName = "Vo Van Viet";
console.log(myName);
// Khai báo biến sử dụng const
// Khi sử dụng const, chúng ta sẽ không thể nào trực tiếp thay đổi (mutate) giá trị của biến được
const yourName = "Dao Thanh Nam";

// Không thể nào mutate được => sẽ lỗi chương trình
yourName = "Ta Quang Minh";
